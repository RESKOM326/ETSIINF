package aed.clase;

import java.lang.Comparable;

@SuppressWarnings("unused")
public class Ninguno {
	
	private int nada;
	private String nulo;
	
	public Ninguno(int nada, String nulo) {
		this.nada=nada;
		this.nulo=nulo;
	}

	public int getNada() {
		return nada;
	}

	public void setNada(int nada) {
		this.nada = nada;
	}

	public String getNulo() {
		return nulo;
	}

	public void setNulo(String nulo) {
		this.nulo = nulo;
	}
	
	

}
